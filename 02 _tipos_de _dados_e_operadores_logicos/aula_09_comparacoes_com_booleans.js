var aula = "Aula 09: Comparações com 'boolean'";
console.log(aula);

/**
 * Maior que: >;
 * Menor que: <;
 * Maior ou igual: >=;
 * Menor ou igual: <=;
 * Igual: ==;
 * Diferente: !=;
 * Idêntico: ===;
 */

console.log(`1 é maior que 2?: ${1 > 2}`);
console.log(`5 é menor que 10?: ${5 < 10}`);
console.log(`3 é maior ou igual a 3?: ${3 >= 3}`);
console.log(`5 é menor ou igual a 4?: ${5 <= 4}`);
console.log(`4 é igual a 4?: ${4 == 4}`);
console.log(`"Gustavo" é diferente de "Gustavo"?: ${"Gustavo" != "Gustavo"}`);
console.log(`3 é idêntico a "3"?: ${3 === "3"}`);
console.log(`4 é idêntico a 4?: ${4 === 4}`);
